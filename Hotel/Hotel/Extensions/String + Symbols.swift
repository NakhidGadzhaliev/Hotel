import Foundation

public extension String {
    static var empty: Self { "" }
    static var space: Self { " " }
    static var comma: Self { "," }
    static var slash: Self { "/" }
    static var dot: Self { "." }
    static var grid: Self { "#" }
    static var currency: Self { "₽" }
    static var minus: Self { "-" }
}
